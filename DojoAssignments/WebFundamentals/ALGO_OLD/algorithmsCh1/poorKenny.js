// Poor Kenny
// Kenny tries to stay safe, but somehow everyday something happens.
// If there is a 10% chance of volcano, 15% chance of tsunami, 20% chance of earthquake,
// 25% chance of blizzard, and 30% chance of meteor strike, write function whatHappensToday()
// to print the outcome.

function whatHappensToday(chance)
{
    if(chance == .10)
    {
        console.log('chance of volcano');
    }
    else if(chance == .15)
    {
        console.log('chance of tsunami');
    }
    else if(chance == .20)
    {
        console.log('chance of earthquake');
    }
    else if(chance == .25)
    {
        console.log('chance of blizzard');
    }
    else if(chance == .30)
    {
        console.log('chance of metor strike');
    }
}

whatHappensToday(.10);